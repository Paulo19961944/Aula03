package oo.java;

public class Aula3Ex02 {
    public static void main(String[] args) {
        // Comandos de Repetição - WHILE
        int num = 1;
        System.out.println("\n---------- WHILE ----------\n");
        while(num < 1500){
            num *= 10;
            System.out.println("Valor de num = " + num);
        }
        System.out.println("\n---------- FIM ---------");
    }
}