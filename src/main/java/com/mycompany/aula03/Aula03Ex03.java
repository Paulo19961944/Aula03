package com.mycompany.aula03;

public class Aula03Ex03 {
    public static void main(String[] args) {
        // Comandos de Laço de Repetição - DO WHILE
        int num  = 10;
        System.out.println("\n---------- DO WHILE ----------\n");
        do{
            System.out.println("O número é maior que 500.");
        } while (num >=100);
        System.out.println("\n---------- FIM ----------\n");
    }
}